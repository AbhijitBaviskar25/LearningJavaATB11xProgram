package ex_03_Operators;

public class Lab015_Modulas_Operator {
	 public static void main(String[] args) {
	        int a = 20;
	        int b = 10;
	        System.out.println(a%b);
	        // Mod -> Remainder,
//	        10 | 20 |  2 - Quoinet
//	           | 20 |
//	           -------
//	             0 - Remainder
//	           ----

	        System.out.println(22%7);    //output=remainder=1
	    }
}
